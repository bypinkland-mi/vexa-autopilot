console.log(JSON.stringify({ ok: counts.blocked === 0, counts, outputFile }, null, 2));

if (args.strict === true && (counts.pending > 0 || counts.blocked > 0)) {
  process.exit(1);
}

async function httpCheck(label, url) {
  try {
    const response = await fetch(url, {
      method: "GET",
      redirect: "follow",
      headers: {
        "User-Agent": "vexa-submission-link-check/0.1"
      }
    });
    const contentType = response.headers.get("content-type") || "unknown";
    return {
      label,
      status: response.status >= 200 && response.status < 400 ? "ready" : "blocked",
      url,
      evidence: `HTTP ${response.status}; content-type=${contentType}`
    };
  } catch (error) {
    return {
      label,
      status: "blocked",
      url,
      evidence: oneLine(error.message)
    };
  }
}

function parseArgs(rawArgs) {
  const parsed = {};
  for (let index = 0; index < rawArgs.length; index += 1) {
    const arg = rawArgs[index];
    if (!arg.startsWith("--")) continue;
    const key = arg.slice(2);
    const next = rawArgs[index + 1];
    if (!next || next.startsWith("--")) {
      parsed[key] = true;
    } else {
      parsed[key] = next;
      index += 1;
    }
  }
  return parsed;
}

function assertHttpUrl(value, label) {
  let url;
  try {
    url = new URL(value);
  } catch {
    throw new Error(`${label} must be a valid URL, got: ${value}`);
  }
  if (!["http:", "https:"].includes(url.protocol)) {
    throw new Error(`${label} must start with http:// or https://, got: ${value}`);
  }
}

function isAllowedVideoUrl(value) {
  const hostname = new URL(value).hostname.toLowerCase();
  return (
    hostname === "youtu.be" ||
    hostname.endsWith("youtube.com") ||
    hostname.endsWith("vimeo.com") ||
    hostname.endsWith("facebook.com") ||
    hostname === "fb.watch"
  );
}

function oneLine(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

function escapeTable(value) {
  return oneLine(value).replaceAll("|", "\\|");
}
