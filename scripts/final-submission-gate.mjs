import { mkdir, writeFile } from "node:fs/promises";
import { spawnSync } from "node:child_process";
import path from "node:path";

const root = process.cwd();
const args = parseArgs(process.argv.slice(2));
const allowPending = args["allow-pending"] === true;
const outputFile = path.join(root, "docs/FINAL_SUBMISSION_GATE_REPORT.md");

const values = {
  cloudUrl: args["cloud-url"] || "",
  videoUrl: args["video-url"] || "",
  proofUrl: args["proof-url"] || ""
};

const steps = [];
const missingInputs = Object.entries(values)
  .filter(([, value]) => !value)
  .map(([key]) => cliFlagFor(key));
const hasFinalUrls = missingInputs.length === 0;

for (const [key, value] of Object.entries(values)) {
  if (value) assertHttpUrl(value, key);
}

if (!hasFinalUrls) {
  steps.push({
    label: "Final public URLs supplied",
    status: "pending",
    evidence: `Missing ${missingInputs.join(", ")}. Run with --cloud-url, --video-url, and --proof-url.`
  });
} else {
  steps.push({
    label: "Final public URLs supplied",
    status: "ready",
    evidence: `cloud=${values.cloudUrl}; video=${values.videoUrl}; proof=${values.proofUrl}`
  });
}

await mkdir(path.dirname(outputFile), { recursive: true });
await writeFile(
  outputFile,
  `---
title: Vexa Final Submission Gate Report
created: 2026-06-30
updated: 2026-06-30
---

# Vexa Final Submission Gate Report

Gate generation is in progress.
`,
  "utf8"
);

steps.push(runStep("Local submission check", "npm", ["run", "check:submission"]));

const auditArgs = ["scripts/audit-submission-readiness.mjs"];
const linkArgs = ["scripts/verify-submission-links.mjs"];
const finalPacketArgs = ["scripts/finalize-devpost-packet.mjs"];
if (hasFinalUrls) {
  const urlArgs = [
    "--cloud-url",
    values.cloudUrl,
    "--video-url",
    values.videoUrl,
    "--proof-url",
    values.proofUrl
  ];
  auditArgs.push(...urlArgs, "--strict");
  linkArgs.push(...urlArgs, "--strict");
  finalPacketArgs.push(...urlArgs, "--skip-cloud-verify");
} else {
  if (!allowPending) {
    auditArgs.push("--strict");
    linkArgs.push("--strict");
  }
}

if (hasFinalUrls || allowPending) {
  steps.push(
    runStep(hasFinalUrls ? "Strict submission readiness audit" : "Submission readiness audit", "node", auditArgs)
  );
  steps.push(
    runStep(
      hasFinalUrls ? "Strict submission link verification" : "Submission link verification",
      "node",
      linkArgs
    )
  );
} else {
  steps.push({
    label: "Strict submission readiness audit",
    status: "pending",
    evidence: "Skipped until final URLs are supplied, or rerun with --allow-pending."
  });
  steps.push({
    label: "Strict submission link verification",
    status: "pending",
    evidence: "Skipped until final URLs are supplied, or rerun with --allow-pending."
  });
}

if (hasFinalUrls) {
  steps.push(runStep("Generate final Devpost packet", "node", finalPacketArgs));
} else {
  steps.push({
    label: "Generate final Devpost packet",
    status: "pending",
    evidence: "Skipped until final URLs are supplied."
  });
}

const counts = {
  ready: steps.filter((step) => step.status === "ready").length,
  pending: steps.filter((step) => step.status === "pending").length,
  blocked: steps.filter((step) => step.status === "blocked").length
};

const markdown = `---
title: Vexa Final Submission Gate Report
created: 2026-06-30
updated: 2026-06-30
---

# Vexa Final Submission Gate Report

Generated by:

\`\`\`bash
npm run final:submission${allowPending ? " -- --allow-pending" : ""}${
  values.cloudUrl ? ` --cloud-url ${values.cloudUrl}` : ""
}${values.videoUrl ? ` --video-url ${values.videoUrl}` : ""}${
  values.proofUrl ? ` --proof-url ${values.proofUrl}` : ""
}
\`\`\`

## Summary

- Ready: ${counts.ready}
- Pending: ${counts.pending}
- Blocked: ${counts.blocked}

## Gate Steps

| Step | Status | Evidence |
| --- | --- | --- |
${steps.map((step) => `| ${escapeTable(step.label)} | ${step.status} | ${escapeTable(step.evidence)} |`).join("\n")}

## Submit Only When

${counts.pending === 0 && counts.blocked === 0 ? "- All gate steps are ready." : "- Do not submit yet. Resolve every pending or blocked gate step first."}
`;

await writeFile(outputFile, markdown, "utf8");

const ok = counts.blocked === 0 && (counts.pending === 0 || allowPending);
console.log(JSON.stringify({ ok, counts, outputFile }, null, 2));

if (!ok) process.exit(1);

function runStep(label, command, commandArgs) {
  const result = spawnSync(command, commandArgs, {
    cwd: root,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"]
  });
  return {
    label,
    status: result.status === 0 ? "ready" : "blocked",
    evidence: compactOutput(result.status === 0 ? result.stdout : result.stderr || result.stdout)
  };
}

function compactOutput(value) {
  const cleaned = oneLine(value);
  return cleaned.length > 900 ? `${cleaned.slice(0, 900)}...` : cleaned;
}

function parseArgs(rawArgs) {
  const parsed = {};
  for (let index = 0; index < rawArgs.length; index += 1) {
    const arg = rawArgs[index];
    if (!arg.startsWith("--")) continue;
    const key = arg.slice(2);
    const next = rawArgs[index + 1];
    if (!next || next.startsWith("--")) {
      parsed[key] = true;
    } else {
      parsed[key] = next;
      index += 1;
    }
  }
  return parsed;
}

function cliFlagFor(key) {
  if (key === "cloudUrl") return "--cloud-url";
  if (key === "videoUrl") return "--video-url";
  if (key === "proofUrl") return "--proof-url";
  return `--${key}`;
}

function assertHttpUrl(value, label) {
  let url;
  try {
    url = new URL(value);
  } catch {
    throw new Error(`${label} must be a valid URL, got: ${value}`);
  }
  if (!["http:", "https:"].includes(url.protocol)) {
    throw new Error(`${label} must start with http:// or https://, got: ${value}`);
  }
}

function oneLine(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

function escapeTable(value) {
  return oneLine(value).replaceAll("|", "\\|");
}
